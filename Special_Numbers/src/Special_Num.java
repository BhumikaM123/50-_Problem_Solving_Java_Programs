
public class Special_Num {
	public static void main(String[] args) {
		int a = 045;//octal
		System.out.println("The integer of octal number is  "+a);
		int b = 0x45;//hexadecimal
		System.out.println("The integer of hexadecimal number is  "+b);
		int c = 0b1100;//binary
		System.out.println("The integer of binary number is  "+c);
		
		
	}

}
